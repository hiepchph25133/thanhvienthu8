    public void LoadSanPham() {
        List<SanPhamViewModel> sp = spservice.getallSanPhamViewModel();
        tableModel = (DefaultTableModel) tbQLSP.getModel();
        tableModel.setRowCount(0);
        for (SanPhamViewModel x : sp) {
            tableModel.addRow(new Object[]{x.getMaSP(), x.getTensp(), x.getTenMS(), x.getTenHang(), x.getSoLuong(), x.getGianhap(), x.getGiaBan()});
        }
    }

    public void loadMauSac() {
        List<MauSac> ms = msserService.getAlMauSacs();
        tableModel = (DefaultTableModel) tbMauSac.getModel();
        tableModel.setRowCount(0);
        for (MauSac m : ms) {
            tableModel.addRow(new Object[]{m.getMaMS(), m.getTenMS()});
        }
    }

    public void loadHang() {
        List<Hang> h = hserService.getAllHangs();
        tableModel = (DefaultTableModel) tbHang.getModel();
        tableModel.setRowCount(0);
        for (Hang m : h) {
            tableModel.addRow(new Object[]{m.getMaHang(), m.getTenHang()});
        }
    }

    public void loadComboMS(ArrayList<MauSac> list) {
        comboBoxModel = (DefaultComboBoxModel) cbMauSac.getModel();
        for (MauSac m : list) {
            comboBoxModel.addElement(m);
        }
    }

    public void loadComboHang(ArrayList<Hang> list) {
        comboBoxModel = (DefaultComboBoxModel) cbHang.getModel();
        for (Hang m : list) {
            comboBoxModel.addElement(m);
        }
    }

    public SanPhamViewModel getInpuViewModel() {
        SanPhamViewModel sp = new SanPhamViewModel();
        MauSac mausac = (MauSac) cbMauSac.getSelectedItem();
        Hang h = (Hang) cbHang.getSelectedItem();
        sp.setTenMS(mausac.getMaMS());
        sp.setTenHang(h.getMaHang());
        sp.setMaSP(txtMaSP.getText());
        sp.setTensp(txtTenSP.getText());
        sp.setSoLuong(Integer.parseInt(txtSoLuong.getText()));
        sp.setGianhap(Double.parseDouble(txtGiaNhap.getText()));
        sp.setGiaBan(Double.parseDouble(txtGiaBans.getText()));
        return sp;
    }

    public MauSac getInputMauSac() {
        MauSac ms = new MauSac();
        ms.setMaMS(txtMaMS.getText());
        ms.setTenMS(txtTenMS.getText());
        return ms;
    }

    public Hang getInputHang() {
        Hang h = new Hang();
        h.setMaHang(txtMaHang.getText());
        h.setTenHang(txtTenHang.getText());
        return h;
    }
