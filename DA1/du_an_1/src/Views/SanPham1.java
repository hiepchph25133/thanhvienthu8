    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        SanPhamViewModel sp = new SanPhamViewModel();
        MauSac mausac = (MauSac) cbMauSac.getSelectedItem();
        Hang h = (Hang) cbHang.getSelectedItem();
        sp.setTenMS(mausac.getMaMS());
        sp.setTenHang(h.getMaHang());
        sp.setMaSP(txtMaSP.getText());
        sp.setTensp(txtTenSP.getText());
        sp.setSoLuong(Integer.parseInt(txtSoLuong.getText()));
        sp.setGianhap(Double.parseDouble(txtGiaNhap.getText()));
        sp.setGiaBan(Double.parseDouble(txtGiaBans.getText()));

        int hoi = JOptionPane.showConfirmDialog(this, "Bạn Có Muốn Sửa Không ?", "Sửa", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (hoi == JOptionPane.YES_OPTION) {
            JOptionPane.showMessageDialog(this, "Sửa Thành Công");
            spservice.Update(sp);
            LoadSanPham();
        }
    }//GEN-LAST:event_btnSuaActionPerformed

